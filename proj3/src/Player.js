class Player {
    constructor(id) {
        this.id = id
    }

    getType() {
        return this.type;
    }

    setType(Type) {
        this.type = Type;
    }

    getColor() {
        return this.color;
    }

    setColor(Color) {
        this.color = Color;
    }

    getName() {
        return this.name;
    }

    setName(Name) {
        this.name = Name;
    }
}